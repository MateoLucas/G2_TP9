int palin(char pal[MAXLETRAS],int size){//determina si es un palindromo o no
    char *p1=&pal[0],*p2=&pal[size-1];//creo dos punteros, uno apunta al principio del string y el otro al final
    if(p2<=p1){//caso base, estoy en el centro
        if(*p2==*p1){//el centro es igual
            return OK;
        }else{//no es palindromo
            return MAL;
        }
            
    }else{//caso recursivo
        if((*p1)==(*p2)){//analizo de afuera hacia adentro, si es igual incremento y decremento punteros
            return (OK+(palin(pal+1,(size-2))));//las demas partes+ el centro
        }else{//no es palindromo
            return MAL;
        }
        
    }
    
}


